# Inherits from Numeric class
# Addition
result = 5 + 3
puts result # Output: 8

# Subtraction
result = 10 - 4 # Output: 6
puts result

# Multiplication
result = 6 * 2
puts result # Output: 12

# Division
result = 15 / 3
puts result # Output: 5

# Modulo (Remainder)
result = 17 % 5
puts result # Output: 2

# Exponentiation
result = 2**3
puts result # Output: 8

# Integer Methods

# abs - returns the absolute value of an integer
result = -10.abs
puts result # Output: 10

# even? - returns true if the integer is even, false otherwise
result = 6.even?
puts result # Output: true

# odd? - returns true if the integer is odd, false otherwise
result = 7.odd?
puts result # Output: true

# next - returns the next integer
result = 5.next
puts result # Output: 6

# times - executes a block of code a specified number of times
3.times { puts "Hello" } # Output: Hello Hello Hello
