# Inherits from Numeric class
# Addition
result = 5 + 3 # Output: 8

# Subtraction
result = 10 - 4 # Output: 6

# Multiplication
result = 6 * 2 # Output: 12

# Division
result = 15 / 3 # Output: 5

# Modulo (Remainder)
result = 17 % 5 # Output: 2

# Exponentiation
result = 2**3 # Output: 8 (2 * 2 * 2)

# Integer Methods

# abs - returns the absolute value of an integer
result = -10.abs # Output: 10

# even? - returns true if the integer is even, false otherwise
result = 6.even? # Output: true

# odd? - returns true if the integer is odd, false otherwise
result = 7.odd? # Output: true

# next - returns the next integer
result = 5.next # Output: 6

# times - executes a block of code a specified number of times
3.times { puts "Hello" } # Output: Hello Hello Hello
